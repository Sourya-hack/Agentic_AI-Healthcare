# Cell 2 — Define training function
def train_model(model, train_loader, val_loader, num_epochs=5, lr=1e-4):

    criterion = nn.BCEWithLogitsLoss()
    optimizer = optim.Adam(model.parameters(), lr=lr, weight_decay=1e-5)
    scheduler = optim.lr_scheduler.ReduceLROnPlateau(
        optimizer, mode='min', patience=2, factor=0.5
    )

    history        = {'train_loss': [], 'val_loss': [], 'val_auc': []}
    best_val_loss  = float('inf')

    for epoch in range(num_epochs):
        # ── Training ──
        model.train()
        train_loss = 0.0

        for images, labels, _ in tqdm(train_loader,
                                       desc=f'Epoch {epoch+1}/{num_epochs} [Train]'):
            images = images.to(device)
            labels = labels.to(device)

            optimizer.zero_grad()
            outputs = model(images)
            loss    = criterion(outputs, labels)
            loss.backward()
            optimizer.step()
            train_loss += loss.item()

        avg_train_loss = train_loss / len(train_loader)

        # ── Validation ──
        model.eval()
        val_loss   = 0.0
        all_preds  = []
        all_labels = []

        with torch.no_grad():
            for images, labels, _ in tqdm(val_loader,
                                           desc=f'Epoch {epoch+1}/{num_epochs} [Val]'):
                images = images.to(device)
                labels = labels.to(device)

                outputs  = model(images)
                loss     = criterion(outputs, labels)
                val_loss += loss.item()

                preds = torch.sigmoid(outputs).cpu().numpy()
                all_preds.append(preds)
                all_labels.append(labels.cpu().numpy())

        avg_val_loss = val_loss / len(val_loader)

        all_preds  = np.vstack(all_preds)
        all_labels = np.vstack(all_labels)

        # Compute mean AUC across all 14 classes
        aucs = []
        for i in range(NUM_CLASSES):
            if len(np.unique(all_labels[:, i])) > 1:
                aucs.append(roc_auc_score(all_labels[:, i], all_preds[:, i]))
        mean_auc = np.mean(aucs) if aucs else 0.0

        scheduler.step(avg_val_loss)

        history['train_loss'].append(avg_train_loss)
        history['val_loss'].append(avg_val_loss)
        history['val_auc'].append(mean_auc)

        print(f"\n📊 Epoch {epoch+1:02d} Summary:")
        print(f"   Train Loss : {avg_train_loss:.4f}")
        print(f"   Val Loss   : {avg_val_loss:.4f}")
        print(f"   Mean AUC   : {mean_auc:.4f}")

        # Save best model
        if avg_val_loss < best_val_loss:
            best_val_loss = avg_val_loss
            torch.save(model.state_dict(), '/content/best_cxr_model.pth')
            print(f"   💾 Best model saved!")

    print(f"\n✅ Training complete! Best Val Loss: {best_val_loss:.4f}")
    return history

