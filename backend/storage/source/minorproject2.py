# CELL 27 — Local LLM query function (using loaded Mistral model)

import json, re, torch

def query_llm(prompt, retries=2):

    formatted_prompt = f"[INST] {prompt} [/INST]"

    inputs = tokenizer(formatted_prompt, return_tensors="pt").to("cuda")

    outputs = model.generate(
        **inputs,
        max_new_tokens=350,
        temperature=0.2
    )

    response = tokenizer.decode(outputs[0], skip_special_tokens=True)

    # Remove prompt from response
    response = response.replace(formatted_prompt, "").strip()

    # Try to extract JSON
    try:
        return json.loads(response)
    except:
        match = re.search(r'\{.*?\}', response, re.DOTALL)
        if match:
            try:
                return json.loads(match.group())
            except:
                pass

    return {
        "ERROR_TYPE": "Unknown",
        "REASONING": response[:200],
        "BIAS_INDICATOR": "Unknown",
        "SEVERITY": "Unknown",
        "RECOMMENDATION": "Manual review needed"
    }

print("✅ Local Mistral LLM query function ready!")

# CELL 26 — Rule-based fallback (in case LLM has issues)
def rule_based_llm_analysis(record):

    true_labels = record['true_labels'] if isinstance(record['true_labels'], list) else record['true_labels'].split(', ')
    fp          = record['false_positives'] if isinstance(record['false_positives'], list) else record['false_positives'].split(', ')
    fn          = record['false_negatives'] if isinstance(record['false_negatives'], list) else record['false_negatives'].split(', ')
    age         = record['age']
    gender      = record['gender']

    fp_clean = [x for x in fp if x and x != 'None']
    fn_clean = [x for x in fn if x and x != 'None']

    OVERLAPPING_PAIRS = [
        ('Infiltration','Consolidation'),
        ('Effusion','Edema'),
        ('Atelectasis','Consolidation'),
        ('Mass','Nodule'),
        ('Emphysema','Pneumothorax'),
        ('Pleural_Thickening','Effusion')
    ]

    if fp_clean and not fn_clean:
        error_type = 'False Positive'
    elif fp_clean and fn_clean:
        pair_found = any((a in fn_clean and b in fp_clean) or (b in fn_clean and a in fp_clean) for a, b in OVERLAPPING_PAIRS)
        error_type = 'Overlapping Feature Confusion' if pair_found else 'Subtle Pathology Error'
    elif fn_clean:
        subtle     = ['Nodule', 'Mass', 'Fibrosis', 'Pleural_Thickening', 'Hernia']
        error_type = 'Subtle Pathology Error' if any(d in fn_clean for d in subtle) else 'False Negative'
    else:
        error_type = 'False Negative'

    high_risk   = ['Pneumothorax', 'Pneumonia', 'Edema', 'Mass', 'Cardiomegaly']
    medium_risk = ['Effusion', 'Consolidation', 'Atelectasis', 'Infiltration']
    all_missed  = fn_clean + fp_clean

    severity = ('High' if any(d in all_missed for d in high_risk)
                else 'Medium' if any(d in all_missed for d in medium_risk)
                else 'Low')

    bias = 'No'
    if age > 0 and (age < 18 or age > 75):
        bias = f'Yes — age group ({age}y) may be underrepresented'
    elif gender == 'F' and any(d in all_missed for d in ['Mass', 'Cardiomegaly']):
        bias = 'Yes — gender-related pathology differences possible'

    missed_str = ', '.join(fn_clean) if fn_clean else 'none'
    over_str   = ', '.join(fp_clean) if fp_clean else 'none'

    reasoning  = f'The model missed {missed_str} and over-predicted {over_str}.'

    return {
        'ERROR_TYPE': error_type,
        'REASONING': reasoning,
        'BIAS_INDICATOR': bias,
        'SEVERITY': severity,
        'RECOMMENDATION': 'Review training data quality.'
    }

print("✅ Rule-based fallback function defined!")

